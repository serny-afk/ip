package chatbot.exception;

/**
 * Base exception class for all exceptions in the chatbot.Anoop project.
 */
public class AnoopException extends Exception {

    /**
     * Constructs an AnoopException with a specified error message.
     *
     * @param message The detail message for the exception.
     */
    public AnoopException(String message) {
        super(message);
    }
}
